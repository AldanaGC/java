
public class bucleWhile {

    public static void main(String[] args) {
        
        //Inicializo y declaro variable
        int c = 0;
        
        //Bucle While

        while(c <= 10){
            //Bloque de sentencias
            System.out.println(c);

            //Actualizacion de la variable
            c++;
        }

    }
}